package inttostringconversion;

public class IntToStringConversion 
{

	public static void main(String[] args)
	{
		//declare an integer
		int myinteger = 10;
		//declare a string.
		String mystring =" ";//its empty string
		// we have to convert the integer 10 to string and we have to print it.
		mystring = Integer.toString(myinteger);
		System.out.println(mystring);
		
	}
}
